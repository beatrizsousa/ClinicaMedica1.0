    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Exame exame = new Exame();
        DaoExame daoExame = new DaoExame();
        Medico medico = new Medico();
        Paciente paciente = new Paciente();
        
        if(!jTextFieldNomePaciente_SolicitarExame.getText().isEmpty() 
                || jTextFieldNomeMedico_SolicitarExame.getText().isEmpty()
                || jTextFieldExames_SolicitarExames.getText().isEmpty()
              
         ) {
            
           paciente.setNome(jTextFieldNomePaciente_SolicitarExame.getText());
           medico.setNome(jTextFieldNomeMedico_SolicitarExame.getText());
           exame.setNome(jTextFieldExames_SolicitarExames.getText());
            

           daoExame.adicionaExame(exame);
                      
        dispose();
        TelaPrincipal_Medico.main(null);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

